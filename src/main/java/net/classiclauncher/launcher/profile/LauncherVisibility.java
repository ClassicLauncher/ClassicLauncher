package net.classiclauncher.launcher.profile;

public enum LauncherVisibility {
	/**
	 * Close the launcher when the game starts.
	 */
	CLOSE_LAUNCHER,
	/**
	 * Keep the launcher window open while the game runs.
	 */
	KEEP_OPEN,
	/**
	 * Hide the launcher when the game starts and reopen it when the game closes.
	 */
	HIDE_LAUNCHER
}
